package com.zhangguoming.maven.web.entity;

public class Area {
	private String id;
	private String code;
	private String name;
	private String pid;
	private String lft;
	private String rgt;
	private String name_en;
	private String short_name_en;
	private String is_show;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	public String getLft() {
		return lft;
	}
	public void setLft(String lft) {
		this.lft = lft;
	}
	public String getRgt() {
		return rgt;
	}
	public void setRgt(String rgt) {
		this.rgt = rgt;
	}
	public String getName_en() {
		return name_en;
	}
	public void setName_en(String name_en) {
		this.name_en = name_en;
	}
	public String getShort_name_en() {
		return short_name_en;
	}
	public void setShort_name_en(String short_name_en) {
		this.short_name_en = short_name_en;
	}
	public String getIs_show() {
		return is_show;
	}
	public void setIs_show(String is_show) {
		this.is_show = is_show;
	}
	
	
}
